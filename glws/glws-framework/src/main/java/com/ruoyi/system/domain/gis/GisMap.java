package com.ruoyi.system.domain.gis;

import com.ruoyi.common.core.domain.BaseEntity;

/**
 * @Describe
 * @Author LiRenDong
 * @DATE 2020/6/23
 */
public class GisMap extends BaseEntity {
    private int id;
    private String name;
    private String type;
    private String geo;
    private String project_type;
    private String centre;
    private String area;
    private String gid;
    private String address;
    private String hazard_rating;

    public String getHazard_rating() {
        return hazard_rating;
    }

    public void setHazard_rating(String hazard_rating) {
        this.hazard_rating = hazard_rating;
    }

    public String getGid() {
        return gid;
    }

    public void setGid(String gid) {
        this.gid = gid;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getGeo() {
        return geo;
    }

    public void setGeo(String geo) {
        this.geo = geo;
    }

    public String getProject_type() {
        return project_type;
    }

    public void setProject_type(String project_type) {
        this.project_type = project_type;
    }

    public String getCentre() {
        return centre;
    }

    public void setCentre(String centre) {
        this.centre = centre;
    }

    public String getArea() {
        return area;
    }

    public void setArea(String area) {
        this.area = area;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public GisMap() {
    }

    public GisMap(int id, String name, String type, String geo, String project_type, String centre, String area, String gid, String address,String hazard_rating) {
        this.id = id;
        this.name = name;
        this.type = type;
        this.geo = geo;
        this.project_type = project_type;
        this.centre = centre;
        this.area = area;
        this.gid = gid;
        this.address = address;
        this.hazard_rating = hazard_rating;
    }
}
