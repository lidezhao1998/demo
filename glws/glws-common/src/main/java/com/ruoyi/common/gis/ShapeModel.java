package com.ruoyi.common.gis;

/**
 * @author lrd
 * @version 1.0
 * @date 2020/4/23 15:14
 */
public class ShapeModel {
    private int id;
    private int pid;
    private String cityName;
    private String f4;
    private String ancestors;
    private float area;
    private float perimeter;
    private String geoStr;
    private String center;

    public ShapeModel() {
    }

    public ShapeModel(int id, int pid, String cityName, String f4, float area, float perimeter, String geoStr,String ancestors,String center) {
        this.id = id;
        this.pid = pid;
        this.cityName = cityName;
        this.f4 = f4;
        this.area = area;
        this.perimeter = perimeter;
        this.geoStr = geoStr;
        this.ancestors = ancestors;
        this.center = center;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getPid() {
        return pid;
    }

    public void setPid(int pid) {
        this.pid = pid;
    }

    public String getCityName() {
        return cityName;
    }

    public void setCityName(String cityName) {
        this.cityName = cityName;
    }

    public String getF4() {
        return f4;
    }

    public void setF4(String f4) {
        this.f4 = f4;
    }

    public float getArea() {
        return area;
    }

    public void setArea(float area) {
        this.area = area;
    }

    public float getPerimeter() {
        return perimeter;
    }

    public void setPerimeter(float perimeter) {
        this.perimeter = perimeter;
    }

    public String getGeoStr() {
        return geoStr;
    }

    public void setGeoStr(String geoStr) {
        this.geoStr = geoStr;
    }
    public String getAncestors() {
        return ancestors;
    }

    public void setAncestors(String ancestors) {
        this.ancestors = ancestors;
    }
    public String getCenter() {
        return center;
    }

    public void setCenter(String center) {
        this.center = center;
    }
}
