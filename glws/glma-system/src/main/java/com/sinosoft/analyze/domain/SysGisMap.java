package com.sinosoft.analyze.domain;

import com.ruoyi.common.core.domain.BaseEntity;

/**
 * @Describe
 * @Author LiRenDong
 * @DATE 2020/6/23
 */
public class SysGisMap extends BaseEntity {
    private int id;
    private String name;
    private String type;
    private String coordinates;
    private String color;

    public SysGisMap() {
    }

    public SysGisMap(int id, String name, String type, String coordinates, String color) {
        this.id = id;
        this.name = name;
        this.type = type;
        this.coordinates = coordinates;
        this.color = color;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getCoordinates() {
        return coordinates;
    }

    public void setCoordinates(String coordinates) {
        this.coordinates = coordinates;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    @Override
    public String toString() {
        return "GisMap{" +
                "id=" + id +
                ", name='" + name + '\'' +
                ", type='" + type + '\'' +
                ", coordinates='" + coordinates + '\'' +
                ", color='" + color + '\'' +
                '}';
    }
}
