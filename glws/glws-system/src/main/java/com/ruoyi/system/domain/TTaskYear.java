package com.ruoyi.system.domain;

import com.ruoyi.common.core.domain.BaseEntity;

/**
 * 退牧还草工程进度年度对象
 *
 * @author feiyanxu
 * @date 2019-12-19
 */
public class TTaskYear extends BaseEntity
{
    private static final long serialVersionUID = 1L;
    private String year;

    public TTaskYear(String year) {
        this.year = year;
    }

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }

    @Override
    public String toString() {
        return "TTaskYear{" +
                "year='" + year + '\'' +
                '}';
    }
}
